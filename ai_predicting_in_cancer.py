import sklearn
from sklearn.datasets import load_breast_cancer
from sklearn.model_selection import train_test_split
from sklearn.naive_bayes import GaussianNB
from sklearn.metrics import accuracy_score

data = load_breast_cancer()
#print(data)

lable_names = data['target_names']
lables = data ['target']
feature_names = data ['feature_names']
features = data ['data']

print(lable_names)
print(lables[0])
print(feature_names)
print(features[0])

#create Training and testing sets:
train , test , train_lable , test_lable = train_test_split(features , lables , test_size=0.33 , random_state=42)

gnb = GaussianNB()

model = gnb.fit (train , train_lable)
pred = gnb.predict(test)
print(pred)

print(accuracy_score(test_lable , pred))